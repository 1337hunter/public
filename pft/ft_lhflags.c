const char	*ft_lhflags(const char *s, int *flag)
{
	while (*s == 'l' || *s == 'h')
	{
		flag[8] = (*s == 'l' && flag[7] ? 1 : 0);
		flag[7] = (*s == 'l' ? 1 : 0);
		flag[10] = (*s == 'h' && flag[9] ? 1 : 0);
		flag[9] = (*s == 'h' ? 1 : 0);
		s++;
	}
	return (s);
}