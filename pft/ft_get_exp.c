#include <stdio.h>
int     ft_get_exp(char *s)
{
    int exp;

    exp = 0;
    if (*s == '0')
    {
        exp--;
        if (s[1] != 0 && s[2] != 0)
        {
            s += 2;
            while (*s == '0' && *s != 0)
            {
                exp--;
                s++;
            }
        }
        if (exp == -52)
            exp = 0;
    }
    else if (*s >= '1' && *s <= '9' && *s != 0)
        while (*(s + 1) != '.')
        {
            exp++;
            s++;
        }
    return (exp);
}