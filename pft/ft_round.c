int	ft_round(char *s)
{
	if (s[1] >= '5' && s[1] <= '9')
		if (s[0] >= '0' && s[0] < '9')
			s[0]++;
		else
		{
			ft_round(s - 1);
			if (s[0] != '.')
				s[0] = '0';
			return (1);
		}
	else if (s[1] == '.')
		if (s[2] >= '5' && s[2] <= '9')
			if (s[0] >= '0' && s[0] < '9')
				s[0]++;
			else
			{
				ft_round(s - 1);
				if (s[0] != '.')
					s[0] = '0';
				return (1);
			}
	return (0);
}
