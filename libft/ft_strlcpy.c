#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	count;

	if (dst == 0)
		return (0);
	i = 0;
	count = 0;
	while (src[count] != '\0')
		count++;
	if (size == 0)
		return (count);
	while (i < size - 1 && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (count);
}
