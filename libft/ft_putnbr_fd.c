#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	str[10];
	int		i;

	i = 10;
	if (n < 0)
	{
		write(fd, "-", 1);
		if (n == ((-1) * 2147483648))
		{
			str[--i] = '8';
			n = n / 10;
		}
		n = -n;
	}
	if (n == 0)
		write(fd, "0", 1);
	while (n != 0)
	{
		str[--i] = '0' + (n % 10);
		n = n / 10;
	}
	while (i < 10)
		write(fd, &str[i++], 1);
}
